#include <bits/stdc++.h>
using namespace std;
int main()
{
    int a, b;
    cout << "Nhap hai so A, B: ";
    cin >> a >> b;
    cout << "Tong: " << a << " + " << b << " = " << a + b << "\n";
    cout << "Hieu: " << a << " - " << b << " = " << a - b << "\n";
    cout << "Tich: " << a << " * " << b << " = " << a * b << "\n";
    cout << "Thuong: " << a << " / " << b << " = " << 1.0 * a / b << "\n";
    return 0;
}
