ll n;
bool prime[10000001];

ll tonguoc(ll n) {
    ll out = 1;
    for (ll i = 2; i * i <= n; i++) {
        if (n % i == 0) {
            out += i;
            if (i != n / i) out += n / i;
        }
    }

    return out;
}

bool check_prime(ll inp) {
    if (inp <= 10000000) {
        return prime[inp];
    } else {
        bool isPrime = true;
        if (inp % 2 == 0 || inp % 3 == 0) return false;
        for (ll i = 5; i * i <= inp; i += 6) {
            if (inp % i == 0 || inp % (i + 2) == 0) return false;
        }
        return true;
    }
}

